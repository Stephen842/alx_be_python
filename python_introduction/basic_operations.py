## This is a basic program that perform basic arithmetics

number1 = 10
number2 = 5

addi = number1 + number2
subt = number2 - number1
mult = number1 * number2

print(f'Addition of {number1} and {number2} is {addi}')
print(f'Subtraction of {number2} and {number1} is {subt}')
print(f'Multiplication of {number1} and {number2} is {mult}')

